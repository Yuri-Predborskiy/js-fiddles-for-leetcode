/*
Find an item in a sorted rotated array that may contain duplicates

Solution using binary search with extra checks for duplicates and state of the half (rotated or not)

Time complexity: O(log(n)) average, O(n) worst case (see tests)
Space complexity: O(1)

Inspired by
https://leetcode.com/problems/search-in-rotated-sorted-array-ii/discuss/28218/
 */

/**
 * @param {number[]} nums
 * @param {number} target
 * @return {boolean}
 */
let search = function(nums, target) {
    let left = 0, right = nums.length - 1;
    while (left <= right) {
        const mid = Math.floor((left + right) / 2);
        if (nums[mid] === target) {
            return true;
        } else if (nums[left] === nums[mid] && nums[right] === nums[mid]) {
            left++;
            right--;
        } else if (nums[left] <= nums[mid]) {
            if (nums[left] <= target && nums[mid] > target) {
                right = mid - 1;
            } else {
                left = mid + 1;
            }
        } else {
            if (nums[mid] < target && nums[right] >= target) {
                left = mid + 1;
            } else {
                right = mid - 1;
            }
        }
    }
    return false;
};

